//{---  Basic struct of GameLib  (VPORT, RECT, GOBJ) ---------
    struct VPORT
    {
        unsigned char Left,Top;        // Coordinates of the top left and bottom right corners of the 
        unsigned char Right,Bottom;    // area visible to sprite drawers.
    };

    struct RECT
    {
        unsigned char X,Y,Width,Height;    // Indicates a rectangle: left-top X,Y, width and height
    };

    struct GOBJ
    {
        int X,Y;			// 16-bit signed coordinates of the sprite
        int SprInd;		// Index of the sprite in the exported session
        unsigned char Flag;	// bit 0: indicates if the sprite is on, bit 7 if it should be mirrored.
        char *SprBackAddr;		// Start address of the mem. block reserved for saving the sprite background. If you do not need to save the background then its value is 0.
        char *SprPalAddr;		// The adress of the sprite palette. If no palette, it is 0.
        int (*spritefunc)();	// Starting address of the sprite drawing routine.
        unsigned char SprWidth;	// Width of the sprite. (*)
        unsigned char SprHeight;	// Height of the sprite. (*)
        char *SprRawAddr;		// Data for the given sprite source (*)
        char *SprScrAddr;		// Start address of sprite after clipping on screen or in background buffer (*)
        unsigned char SprClipSkipRow;    // At the top cut of the sprite, this variable stores how many rows the sprite cutter should skip at the top (*)
        struct RECT SprClip;	// Dimensions of the sprite after clipping (*)
        struct RECT SprPrevClip;	// Post-cut dimensions of the sprite interpreted in the previous phase (*)
        unsigned char SprClipFlag;	// Save the sprite cut state to the sprite drawer (*)
    };
//}


